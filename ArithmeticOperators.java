package JavaPrograms100;

//The Arithmetic class.
class Arithmetic {

  public void work() {

    int x = 17, y = 5;

    System.out.println("x = " + x);

    System.out.println("y = " + y);

    System.out.println("x + y = " + (x + y));

    System.out.println("x - y = " + (x - y));

    System.out.println("x * y = " + (x * y));

    System.out.println("x / y = " + (x / y));

    System.out.println("x % y = " + (x % y));

  }

}
