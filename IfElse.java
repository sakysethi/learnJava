package JavaPrograms100;

// Example program to demostrate the
// usage of if else statement

class IfElse {
    public void demoifelse() {
        byte amount = 10;
        float account = 12.32F;
        if (amount == 10 && account < 10) {
            System.out.println("The value is " + account);
        } else {
            System.out.println("The value is " + amount);
        }
    }
}