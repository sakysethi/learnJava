package JavaPrograms100;

// Program to implement arithmetic on float values.
class ArithmeticOperatorsFloatValues {

  public void work() {

    float x = 23.5F, y = 7.3F;

    System.out.println("x = " + x);

    System.out.println("y = " + y);

    System.out.println("x + y = " + (x + y));

    System.out.println("x - y = " + (x - y));

    System.out.println("x * y = " + (x * y));

    System.out.println("x / y = " + (x / y));

    System.out.println("x % y = " + (x % y));

  }

}
