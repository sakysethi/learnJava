package JavaPrograms100;

// Program to demonstrate Bitwise Operation In JAVA
class Bitwise {

  public void work() {

    int x = 5, y = 6;

    System.out.println("x = " + x);

    System.out.println("y = " + y);

    System.out.println("x & y = " + (x & y));

    System.out.println("x | y = " + (x | y));

    System.out.println("x ^ y = " + (x ^ y));

  }

}
