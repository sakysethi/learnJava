package JavaPrograms100;

// Program to implement icrement and decrement operators
public class IcrementDecrementOperator {

  public void work() {

    int x = 8, y = 13;

    System.out.println("x = " + x);

    System.out.println("y = " + y);

    System.out.println("++x = " + ++x);

    System.out.println("y++ = " + y++);

    System.out.println("x = " + x);

    System.out.println("y = " + y);

  }

}
